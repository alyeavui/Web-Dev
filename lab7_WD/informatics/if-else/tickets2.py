n = int(input())

b60 = n // 60
n %= 60

b20 = n // 20
n %= 20

b10 = n // 10
n %= 10

b5 = n // 5
n %= 5

b1 = n

if b1 * 15 > 70:
    b5 += 1
    b1 = 0

if b10 * 125 + b5 * 70 + b1 * 15 > 230:
    b20 += 1
    b10 = 0
    b5 = 0
    b1 = 0

if b20 * 230 + b10 * 125 + b5 * 70 + b1 * 15 > 440:
    b60 += 1
    b20 = 0
    b10 = 0
    b5 = 0
    b1 = 0

print(b1, b5, b10, b20, b60)
